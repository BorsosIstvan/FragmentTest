package com.simpson;

public class PersonPayload {
	
	String fn;
	String gn;
	String fnt;
	String gnt;
	public String getFn() {
		return fn;
	}
	public void setFn(String fn) {
		this.fn = fn;
	}
	public String getGn() {
		return gn;
	}
	public void setGn(String gn) {
		this.gn = gn;
	}
	public String getFnt() {
		return fnt;
	}
	public void setFnt(String fnt) {
		this.fnt = fnt;
	}
	public String getGnt() {
		return gnt;
	}
	public void setGnt(String gnt) {
		this.gnt = gnt;
	}
	

}
